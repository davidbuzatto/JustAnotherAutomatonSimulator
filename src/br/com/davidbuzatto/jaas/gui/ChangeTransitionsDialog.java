    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        
        List<Transition> selectedT = listTransitions.getSelectedValuesList();
        
        removedTransitions.addAll( selectedT );
        
        DefaultListModel<Transition> dlm = (DefaultListModel<Transition>) listTransitions.getModel();
        for ( Transition t : selectedT ) {
            dlm.removeElement( t );
        }
        
    }//GEN-LAST:event_btnRemoveActionPerformed

