    public ChangeTransitionsDialog( java.awt.Frame parent, State state, boolean modal ) {
        
        super( parent, modal );
        this.state = state;
        initComponents();
        removedTransitions = new ArrayList<>();
        
        setTitle( String.format( "State \"%s\" transitions", state ) );
        
        List<Transition> avT = new ArrayList<>();
        avT.addAll( state.getTransitions() );
        
        Collections.sort( avT, new Comparator<Transition>(){
            @Override
            public int compare( Transition o1, Transition o2 ) {
                int c1 = o1.getTarget().getNumber() - o2.getTarget().getNumber();
                if ( c1 == 0 ) {
                    return o1.getSymbol() - o2.getSymbol();
                } else {
                    return c1;
                }
            }
        });
        
        DefaultListModel<Transition> dlm = new DefaultListModel<>();
        listTransitions.setModel( dlm );
        dlm.addAll( avT );
        
    }

